
#Python programming to draw pentagon in turtle programming
import turtle
 
t = turtle.Turtle()
for i in range(5):
   t.forward(100) #Assuming the side of a pentagon is 100 units
   t.right(72) #Turning the turtle by 72 degree
  
  
